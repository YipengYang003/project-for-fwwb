/**
 * LawArticle: 单条法律条款的数据结构
 * - num: 条款编号（如"第一条"、"第一百八十八条"）
 * - title: 条款标题（如"诉讼时效期间"），可选
 * - content: 条款完整内容
 */
interface LawArticle {
  num: string;          // 条款编号，如"第一条"
  title?: string;        // 条款标题，如"立法目的"（可选）
  content: string;       // 条款的完整法律文本内容
}

/**
 * Law: 一部完整法律法规的数据结构
 * - id: 法律法规的唯一标识符，用于 React key 和链接跳转
 * - name: 法律法规全称
 * - shortName: 简称（如"民法典"、"劳动合同法"）
 * - category: 法律所属类别（用于分类筛选）
 * - level: 效力等级（法律、行政法规、司法解释）
 * - issueDate: 法律发布日期
 * - effectDate: 法律施行日期
 * - issuer: 发布机关（全称）
 * - summary: 法律简介/立法目的说明
 * - articles: 该法律包含的条款数组
 * - officialUrl: 全国人大数据库中的官方页面链接
 */
interface Law {
  id: string;            // 唯一标识符
  name: string;          // 法律全称
  shortName?: string;    // 常用简称
  category: string;      // 分类：民事、劳动、消费、刑事等
  level: string;         // 效力等级：法律、行政法规、司法解释
  issueDate: string;      // 发布日期（ISO 格式）
  effectDate: string;     // 施行日期（ISO 格式）
  issuer: string;        // 发布机关名称
  summary: string;       // 法律简介/核心内容概述
  articles: LawArticle[]; // 重要条款列表
  officialUrl: string;   // 官方数据库链接
}

